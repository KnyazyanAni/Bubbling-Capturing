/*************Events on Box1*****************/
box1.onmouseover= function colorChange(event)
{
event.target.style.backgroundColor="#FF0000";
box1.onmouseover="event.stopPropagation()";
  };
box1.onmouseout=function colorReChange(event)
{
event.target.style.backgroundColor="#AA1100";
box1.onmouseout="event.stopPropagation()";
};
